// Operator perbangdingan akan menghasilkan nilai boolean

// == ( Sama Dengan )
// 10 == 10 ( apakah angka 10 sama dengan 10 = TRUE )

// != ( Tidak Sama Dengan )
// 10 != 10 ( apakah angka 10 tidak sama dengan 10 = FALSE )

// === ( Strict Sama Dengan ) [ === membandingkan dengan tipe data juga]
// 10 === "10" ( apakah angka 10 sama dengan string 10 = FALSE )

// !== ( Strict Tidak Sama Dengan ) [ !== membandingkan dengan tipe data juga]
// 10 !== "10" ( apakah angka 10 tidak sama dengan string 10 = TRUE )

// > ( Lebih Besar Dari )
// 10 > 5 ( apakah 10 lebih besar dari 5 = TRUE )

// < ( Lebih Kecil Dari )
// 10 < 5 ( apakah 10 lebih kecil dari 5 = FALSE )

// >= ( Lebih besar sama dengan )
// 10 >= 10 ( apakah 10 lebih besar sama dengan 10 = TRUE )

// <= ( lebih Kecil sama dengan )
// 10 <= 9 ( apakah 10 lebih kecil sama dengan 9 = FALSE )