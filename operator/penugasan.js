// += 
// isi variable akan ditambahkan sebelum isi variable itu diganti 
//(x = 10, x += 5, x = 15) atau (x += y, x = x + y)

// -= 
// isi variable akan dikurangkan sebelum isi variable itu diganti
// (x = 10, x -= 5, x = 5) atau (x -= y, x = x - y)

// *= 
// isi variable akan dikalikan sebelum isi variable itu diganti
// (x = 10, x *= 5, x = 50) atau (x *= y, x = x * y)

// /=
// isi variable akan dibagi sebelum isi variable itu diganti
// (x = 10, x /= 5, x = 2) atau (x /= y, x = x / y)

// %= 
// isi variable akan dimoduluskan sebelum isi variable itu diganti
//  (x = 10, x %= 5, x = 0) atau (x %= y, x = x % y)